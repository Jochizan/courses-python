nombre = input('¿Cuál es tu nombre?: ')
"""
    Upper levanta todas las letras de mi palabra
    Capitalize levanta solo la primera letra
    Strip borra espacios innecesarios de la cadena
    Lower pone en minuscula todas las letras
    Replace reemplaza el primer argumento mandado por el segundo
    Len devuelve la cantidad de letras de nuestra cadena
"""
print(nombre.upper())
print(nombre.capitalize())
print(nombre.strip())
print(nombre.lower())
print(nombre.replace('o', 'a'))
