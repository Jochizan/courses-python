def sumar(op1, op2):
    print("El resultado de la suma es: ", op1 + op2)

def restar(op1, op2):
    print("El resultado de la resta es: ", op1 - op2)

def multiplicar(op1, op2):
    print("El resultado de la multiplicación es: ", op1 * op2)

def dividir(op1, op2):
    print("El resultado de la división es: ", op1 / op2)

def potencia(base, exponente):
    print("El resultado de llevar dicho número a la " + str(exponente) + " es: ", base**exponente)

def redondear(número):
    print("El resultado de redondear dicho número es: ", round(número))